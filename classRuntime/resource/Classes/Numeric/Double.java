public class Double {

	private double number;

	private void add19() {
		number += 19.0;
	}

	public Double() {
		number = 19.0;
	}

	public static void main(String[] args) {
		Double f = new Double();
		System.out.print(f.number);
		System.out.print("\n");
		f.add19();
		System.out.println(f.number);
	}

}
